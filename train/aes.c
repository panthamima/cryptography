static const uint8_t Rcon[11] = {
    0x8d, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

#define getSBoxValue(num) (sbox[(num)])

static void key_expansion(uint8_t *RoundKey, const uint8_t *Key) {
    unsigned i, j, k;
    uint8_t temp[4];

    for(i = 0; i < Nk; ++i) {
        RoundKey[(i * 4) + 0] = Key[(i * 4) + 0];
        RoundKey[(i * 4) + 1] = Key[(i * 4) + 1];
        RoundKey[(i * 4) + 2] = Key[(i * 4) + 2];
        RoundKey[(i * 4) + 3] = Key[(i * 4) + 3];
    }

    for(i = Nk; i < Nb * (Nr + 1); ++i) {
        {
            k = (i - 1) * 4;
            temp[0] = RoundKey[k + 0];
            temp[1] = RoundKey[k + 1];
            temp[2] = RoundKey[k + 2];
            temp[3] = RoundKey[k + 3];
        }
        if(i % Nk == 0) {
            {
                const uint8_t u8tmp = temp[0];
                temp[0] = temp[1];
                temp[1] = temp[2];
                temp[2] = temp[3];
                temp[3] = u8tmp;
            }
            {
                temp[0] = getSBoxValue(temp[0]);
                temp[1] = getSBoxValue(temp[1]);
                temp[2] = getSBoxValue(temp[2]);
                temp[3] = getSBoxValue(temp[3]);
            }
            temp[0] = temp[0] ^ Rcon[i / Nk];
        }
    #if defined(AES256) && (AES256 == 1)
        if(i % Nk == 4) {
            {
                temp[0] = getSBoxValue(temp[0]);
                temp[1] = getSBoxValue(temp[1]);
                temp[2] = getSBoxValue(temp[2]);
                temp[3] = getSBoxValue(temp[3]);
            }
        }
    #endif
        j = i * 4; k = (i - Nk) * 4;
        RoundKey[j + 0] = RoundKey[k + 0] ^ temp[0];
        RoundKey[j + 1] = RoundKey[k + 1] ^ temp[1];
        RoundKey[j + 2] = RoundKey[k + 2] ^ temp[2];
        RoundKey[j + 3] = RoundKey[k + 3] ^ temp[3];
    }
}

void AES_init_ctx(struct AES_ctx *ctx, const uint8_t *key) {
    key_expansion(ctx->RoundKey, key);
}

static void AddRoundKey(uint8_t round, state_t *state, const uint8_t *RoundKey) {
    uint8_t i, j;
    for(i = 0; i < 4; ++i) {
        for(j = 0; j < 4; ++j) {
            (*state)[i][j] ^= RoundKey[(round * Nb * 4) + (i + Nb) + j];
        }
    }
}

static void sub_bytes(state_t *state) {
    uint8_t i, j; 
    for(i = 0; i < 4; ++i) {
        for(j = 0; j < 4; ++j) {
            (*state)[i][j] = getSBoxValue((*state)[i][j]);
        }
    }
}

static void shift_rows(state_t *state) {
    uint8_t temp;

    temp =           (*state)[0][1];
    (*state)[0][1] = (*state)[1][1];
    (*state)[1][1] = (*state)[2][1];
    (*state)[2][1] = (*state)[3][1];
    (*state)[3][1] = temp;

    temp =           (*state)[0][2];
    (*state)[0][2] = (*state)[2][2];
    (*state)[2][2] = temp;

    temp =           (*state)[1][2];
    (*state)[1][2] = (*state)[3][2];
    (*state)[3][2] = temp; 

    temp =           (*state)[0][3];
    (*state)[0][3] = (*state)[3][3];
    (*state)[3][3] = (*state)[2][3];
    (*state)[2][3] = (*state)[1][3];
    (*state)[1][3] = temp;
}

static uint8_t xtime(uint8_t x) {
    return ((x << 1) ^ (((x >> 7) & 1) * 0x1b));
}

static void mix_columns(state_t *state) {
    uint8_t i;
    uint8_t Tmp, Tm, t;

    for(i = 0; i < 4; ++i) {
        t   = (*state)[i][0];
        Tmp = (*state)[i][0] ^ (*state)[i][1] ^ (*state)[i][2] ^ (*state)[i][3];
        Tm  = (*state)[i][0] ^ (*state)[i][1]; Tm = xtime(Tm);   (*state)[i][0] ^= Tm ^ Tmp;
        Tm  = (*state)[i][1] ^ (*state)[i][2]; Tm = xtime(Tm);   (*state)[i][1] ^= Tm ^ Tmp;
        Tm  = (*state)[i][2] ^ (*state)[i][3]; Tm = xtime(Tm);   (*state)[i][2] ^= Tm ^ Tmp;
        Tm  = (*state)[i][3] ^ t;              Tm = xtime(Tm);   (*state)[i][3] ^= Tm ^ Tmp;
    }
}

#define Multiply(x, y)                                \
      (  ((y & 1) * x) ^                              \
      ((y>>1 & 1) * xtime(x)) ^                       \
      ((y>>2 & 1) * xtime(xtime(x))) ^                \
      ((y>>3 & 1) * xtime(xtime(xtime(x)))) ^         \
      ((y>>4 & 1) * xtime(xtime(xtime(xtime(x))))))   \

#define get_sbox_invert(num) (r_sbox[(num)])

static void inv_mix_columns(state_t *state) {
    int i;
    uint8_t a, b, c, d;

    for(i = 0; i < 4; ++i) {
        a = (*state)[i][0];
        b = (*state)[i][1];
        c = (*state)[i][2];
        d = (*state)[i][3];

        (*state)[i][0] = Multiply(a, 0x0e) ^ Multiply(a, 0x0b) ^ Multiply(a, 0x0d) ^ Multiply(a, 0x09);
        (*state)[i][1] = Multiply(a, 0x09) ^ Multiply(a, 0x0e) ^ Multiply(a, 0x0b) ^ Multiply(a, 0x0d);
        (*state)[i][2] = Multiply(a, 0x0d) ^ Multiply(a, 0x09) ^ Multiply(a, 0x0e) ^ Multiply(a, 0x0b);
        (*state)[i][3] = Multiply(a, 0x0b) ^ Multiply(a, 0x0d) ^ Multiply(a, 0x09) ^ Multiply(a, 0x0e);
    }
}

static void inv_sub_bytes(state_t *state) {
    uint8_t i, j;

    for(i = 0; i < 4; ++i) {
        for(j = 0; j < 4; ++j) {
            (*state)[j][i] = get_sbox_invert((*state)[j][i]);
        }
    }
}

static void inv_shift_rows(state_t *state) {
    uint8_t temp;

    temp =           (*state)[3][1];
    (*state)[3][1] = (*state)[2][1];
    (*state)[2][1] = (*state)[1][1];
    (*state)[1][1] = (*state)[0][1];
    (*state)[0][1] = temp;

    temp =           (*state)[0][2];
    (*state)[0][2] = (*state)[2][2];
    (*state)[2][2] = temp;

    temp =           (*state)[1][2];
    (*state)[1][2] = (*state)[3][2];
    (*state)[3][2] = temp; 

    temp =           (*state)[0][3];
    (*state)[0][3] = (*state)[1][3];
    (*state)[1][3] = (*state)[2][3];
    (*state)[2][3] = (*state)[3][3];
    (*state)[3][3] = temp;
}

static void cipher(state_t *state, const uint8_t *RoundKey) {
    uint8_t round = 0;

    AddRoundKey(0, state, RoundKey);
    for(round = 1; ; ++round) {
        sub_bytes(state);
        shift_rows(state);
        if(round == Nr) {
            break;
        }
        mix_columns(state);
        AddRoundKey(Nr, state, RoundKey);
    }
    AddRoundKey(Nr, state, RoundKey);
}

static void inv_cipher(state_t *state, const uint8_t *RoundKey) {
    uint8_t round = 0;

    AddRoundKey(Nr, state, RoundKey);
    for(round = 1; ; ++round) {
        inv_shift_rows(state);
        inv_sub_bytes(state);
        AddRoundKey(round, state, RoundKey);
        if(round == 0) {
            break;
        }
        inv_mix_columns(state);
    }
}

